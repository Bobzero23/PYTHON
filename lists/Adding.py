#Demonstration on how to add items in the list

#Creating a list
names = ["Bob", "Kombo", "Oma", "Mido", "Mamalao", "Mamba", "Kitufe"]

#Displaying the list
print(names)

#Adding a name to the list
names.append("Sheby")

#Displaying the list
print(names)

#Creating lists
country = []
cities = ["Dar", "Dodoma", "Tanga", "Zanzibar"]

#Adding a list to another list
country.extend(cities)

#Displaying the list
print(country)
