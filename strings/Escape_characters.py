#Demonstration on how to use escape Characters

#For double quotes \"
from cmath import pi


print("\"Hello\"")

#For single quotes
print("\'Hello\'")

#For backslash
print("\\Hello\\")

#For new line
print("Hello\nWorld")

#For carriage return
print("Hello\rWorld")

#For tab
print("Hello\tWorld")

#For Backspace
print("Hello\bWorld")

#For Hex values
print("\x48\x65\x6c\x6c\x6f")

#For Octal values
print("\110\145\154\154\157")