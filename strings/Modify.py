#Demonstration on how to modify Strings

#UpperCase
a = "at the night show"
print(a.upper())

#LowerCase
b = "ANYTHING CAN HAPPEN"
print(b.lower())

#Removing the whitespace at the start and end
c = " Hospital "
print(c.strip())

#Replacing Strings
d = "hahadahaha"
print(d.replace("d","h"))

#Splitting Strings
e = "Split, here."
print(e.split(","))